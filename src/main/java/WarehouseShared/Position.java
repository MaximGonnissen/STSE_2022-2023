package WarehouseShared;

/**
 * Position class to store the position of anything in the system.
 *
 * @author Maxim
 * @author Thimoty
 * @since 10/12/2022
 */
public class Position {
    public float x;
    public float y;

    public Position(float x, float y) {
        this.x = x;
        this.y = y;
    }
}
